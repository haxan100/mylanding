	$(document).ready(function() {

		var bu = '<?= base_url(); ?>';
		$('#send_message').on('click', function() {

			var url_form_tambah = bu + 'User/tambah_komen_proses';

			url_form = url_form_tambah;

			var nama = $('#name').val();
			var email = $('#email').val();
			var message = $('#message').val();
			console.log(nama, email, message)

			if (
				email
			) {
				$("#form").submit();
			}
		});


		$("#form").submit(function(e) {
			console.log('form submitted');
			// return false;
			$.ajax({
				url: url_form,
				method: 'post',
				dataType: 'json',
				data: new FormData(this),
				processData: false,
				contentType: false,
				cache: false,
				async: false,
			}).done(function(e) {
				console.log(e);
				// return false
				if (e.status) {
					Swal.fire('Terimakasih, Komentar Anda Kami simpan')
					console.log("berhasil")
				} else {
					Swal.fire({
						icon: 'error',
						title: 'Oops...',
						text: 'Something went wrong!',
						footer: '<a href>Why do I have this issue?</a>'
					})

				}
			}).fail(function(e) {
				console.log("gagal")
			});
			return false;
		});


	});
